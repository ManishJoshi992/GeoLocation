CREATE TABLE LOCATION (
     ID INT  PRIMARY KEY,	
     IPADDR VARCHAR(255) ,
     CONTINENT VARCHAR(255),
     CONTINENT_CODE VARCHAR(255),
     COUNTRY VARCHAR(255) ,
     COUNTRY_CODE VARCHAR(255) ,
     REGION VARCHAR(255) ,
     REGION_NAME VARCHAR(255) ,
     CITY VARCHAR(255) ,
     DISTRICT VARCHAR(255),
     ZIP VARCHAR(255) ,
     LATITUDE FLOAT ,
     LONGITUDE FLOAT ,
     TIME_ZONE VARCHAR(255)  
);